// String DataType
const firstName: string = "Vighnesh";
console.log(firstName);

const lastName: string = "Tirakapadi";
console.log(lastName);

// Number DataType
const age: number = 22;
console.log(age);

const today: number = 14092023;
console.log(today);

// Boolean DataType
const isMarried: boolean = false;
console.log(isMarried);

const studying: boolean = true;
console.log(studying);

// Array DataType
const arr1: string[] = ["A", "B", "C", "D", "E"];
console.log(arr1);

const arr2: number[] = [1, 2, 3, 4, 5, 6];
console.log(arr2);

const arr3: boolean[] = [true, false];
console.log(arr3);

const arr4: any[] = ["Vighnesh", 22, false, 57.2];
console.log(arr4);

// Any DataType
const firstName1: any = "Vig";
console.log(firstName1);

const year: any = 2023;
console.log(year);
